<dialog id="custom_tile_display_help_dialog" style="display: none;">

    <h3>Hidden</h3>
    <ul>
        <li>- Custom tile will never be displayed.</li>
    </ul>
    <br>

    <h3>Visible</h3>
    <ul>
        <li>- Custom tile will always be shown.</li>
    </ul>
    <br>

    <h3>Custom</h3>
    <ul>
        <li>- Custom tile display will be dynamic and governed by selected field conditions.</li>
        <li>- In order to create a condition, select a field option currently assigned to custom tile and add to
            conditions table.
        </li>
        <li>- Multiple conditions can be specified.</li>
        <li>- If <i>Require all selected condition to be true</i> is selected, then <strong>all</strong> conditions must contain valid
            values in order for custom tile to be shown. However, if this option is unchecked, then the custom tile will
            be displayed if <strong>any</strong> condition is satisfied.
        </li>
    </ul>

</dialog>
